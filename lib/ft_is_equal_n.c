#include "libft.h"

int		ft_is_equal_n(char *s1, char *s2, size_t i_now, size_t n)
{
	size_t i;

	i = 0;
	while (s1[i] == s2[i] && i + i_now < n)
	{
		i++;
	}
	if (s2[i] == '\0')
		return (1);
	return (0);
}
