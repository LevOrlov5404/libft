#include "libft.h"

int		ft_atoi(const char *str)
{
	char	*s;
	int		i;
	int		sign;

	s = (char*)str;
	i = 0;
	sign = 1;
	while (*s == '\t' || *s == '\n'
			|| *s == '\v' || *s == '\f' || *s == '\r' || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		i = i * 10 + *s - '0';
		s++;
	}
	return (i * sign);
}
